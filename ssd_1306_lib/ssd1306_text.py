def add_text(x, y, text):
    """Add text at position (x, y) - y must be multiple of 8"""
    page = y // 8
    
    # Set position
    i2c.write(ADDR, bytes([0x00, 0xB0 + page]))  # Page
    i2c.write(ADDR, bytes([0x00, 0x00 | (x & 0x0F)]))  # Lower column
    i2c.write(ADDR, bytes([0x00, 0x10 | ((x >> 4) & 0x0F)]))  # Higher column
    
    # Write characters
    for char in text:
        if char.upper() in FONT:
            glyph = FONT[char.upper()]
            i2c.write(ADDR, bytes([0x40] + glyph + [0x00]))  # Character + space
        else:
            i2c.write(ADDR, bytes([0x40] + [0x00] * 6))  # Space for unknown char